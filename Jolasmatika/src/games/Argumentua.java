package games;


/**
 * 
 */
public class Argumentua {


    private String mota;
    private String izena;

    /**
     * Constructor
     * @param mota 
     * @param izena
     */
    public Argumentua(String mota, String izena) {
        this.mota = mota;
        this.izena = izena;
    }

    /**
     * @return mota
     */
    public String getMota() {
        return mota;
    }

    /**
     * @return izena
     */
    public String getIzena() {
        return izena;
    }

}